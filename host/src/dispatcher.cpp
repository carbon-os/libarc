CommandDispatcher::CommandDispatcher(ipc::Server& server, WindowRegistry& registry)
    : server_(server), registry_(registry) {}

void CommandDispatcher::emit(nlohmann::json evt) {
    server_.send(std::move(evt));
}

void CommandDispatcher::dispatch(const ipc::Message& msg) {
    if (msg.type != ipc::MessageType::JSON) return;

    const auto j = msg.json();
    if (!j.contains("type") || !j["type"].is_string()) return;

    const std::string t = j["type"];

    if      (t == "host.configure")            on_host_configure(j);
    else if (t == "host.ping")                 on_host_ping();
    else if (t == "host.shutdown")             on_host_shutdown();

    else if (t == "window.create")             on_window_create(j);
    else if (t == "window.destroy")            on_window_destroy(j);
    else if (t == "window.set_title")          on_window_set_title(j);
    else if (t == "window.set_size")           on_window_set_size(j);
    else if (t == "window.set_position")       on_window_set_position(j);
    else if (t == "window.center")             on_window_center(j);
    else if (t == "window.show")               on_window_show(j);
    else if (t == "window.hide")               on_window_hide(j);
    else if (t == "window.focus")              on_window_focus(j);
    else if (t == "window.minimize")           on_window_minimize(j);
    else if (t == "window.maximize")           on_window_maximize(j);
    else if (t == "window.restore")            on_window_restore(j);
    else if (t == "window.set_fullscreen")     on_window_set_fullscreen(j);
    else if (t == "window.set_min_size")       on_window_set_min_size(j);
    else if (t == "window.set_max_size")       on_window_set_max_size(j);
    else if (t == "window.set_always_on_top")  on_window_set_always_on_top(j);
    else if (t == "window.set_effect")         on_window_set_effect(j);
    else if (t == "window.clear_effect")       on_window_clear_effect(j);

    else if (t == "webview.create")            on_webview_create(j);
    else if (t == "webview.destroy")           on_webview_destroy(j);
    else if (t == "webview.load_url")          on_webview_load_url(j);
    else if (t == "webview.load_html")         on_webview_load_html(j);
    else if (t == "webview.load_file")         on_webview_load_file(j);
    else if (t == "webview.reload")            on_webview_reload(j);
    else if (t == "webview.go_back")           on_webview_go_back(j);
    else if (t == "webview.go_forward")        on_webview_go_forward(j);
    else if (t == "webview.eval")              on_webview_eval(j);
    else if (t == "webview.set_zoom")          on_webview_set_zoom(j);
    else if (t == "webview.send_ipc")          on_webview_send_ipc(j);
    else if (t == "webview.set_position")      on_webview_set_position(j);
    else if (t == "webview.set_size")          on_webview_set_size(j);
    else if (t == "webview.show")              on_webview_show(j);
    else if (t == "webview.hide")              on_webview_hide(j);
    else if (t == "webview.set_zorder")        on_webview_set_zorder(j);
}

// ── Host ──────────────────────────────────────────────────────────────────────

void CommandDispatcher::on_host_configure(const nlohmann::json& j) {
    if (j.contains("app_name") && j["app_name"].is_string())
        app_name_ = j["app_name"];
    emit({ {"type", "host.configured"} });
}

void CommandDispatcher::on_host_ping() {
    emit({ {"type", "host.pong"} });
}

void CommandDispatcher::on_host_shutdown() {
    arc::quit_app();
}

// ── Window ────────────────────────────────────────────────────────────────────

void CommandDispatcher::on_window_create(const nlohmann::json& j) {
    const std::string id    = j.value("id", "");
    const std::string style = j.value("style", "default");

    ui::WindowStyle wstyle = ui::WindowStyle::Default;
    if      (style == "border_only") wstyle = ui::WindowStyle::BorderOnly;
    else if (style == "borderless")  wstyle = ui::WindowStyle::Borderless;

    ui::WindowConfig cfg;
    cfg.title     = j.value("title", "");
    cfg.size      = { j.value("width", 800), j.value("height", 600) };
    cfg.resizable = j.value("resizable", true);
    cfg.style     = wstyle;

    auto* mw = registry_.create_window(id, std::move(cfg));
    if (!mw) return;

    wire_window_events(id, *mw->win);

    // Emit initial geometry so Go has accurate values before any resize/move
    // events are generated by the user.
    const auto sz  = mw->win->get_size();
    const auto pos = mw->win->get_position();
    // Report content-area size (same value the fixed windowDidResize sends).
    emit({ {"type", "window.resized"}, {"id", id},
           {"width", sz.width}, {"height", sz.height} });
    emit({ {"type", "window.moved"}, {"id", id},
           {"x", pos.x}, {"y", pos.y} });
}

void CommandDispatcher::wire_window_events(const std::string& id, ui::Window& win) {
    win.on_resize([this, id](ui::Size s) {
        emit({ {"type", "window.resized"}, {"id", id},
               {"width", s.width}, {"height", s.height} });
    });

    win.on_move([this, id](ui::Point p) {
        emit({ {"type", "window.moved"}, {"id", id}, {"x", p.x}, {"y", p.y} });
    });

    win.on_focus([this, id]() {
        emit({ {"type", "window.focused"}, {"id", id} });
    });

    win.on_blur([this, id]() {
        emit({ {"type", "window.blurred"}, {"id", id} });
    });

    win.on_state_change([this, id](ui::WindowState state) {
        std::string s;
        switch (state) {
            case ui::WindowState::Normal:     s = "normal";     break;
            case ui::WindowState::Minimized:  s = "minimized";  break;
            case ui::WindowState::Maximized:  s = "maximized";  break;
            case ui::WindowState::Fullscreen: s = "fullscreen"; break;
        }
        emit({ {"type", "window.state_changed"}, {"id", id}, {"state", s} });
    });

    win.on_close([this, id]() -> bool {
        emit({ {"type", "window.closed"}, {"id", id} });
        return true;
    });
}

