// ── Effect helpers ────────────────────────────────────────────────────────────

std::optional<ui::BackdropEffect>
CommandDispatcher::parse_effect(const std::string& name) {
    if (name == "vibrancy")  return ui::BackdropEffect::Vibrancy;
    if (name == "acrylic")   return ui::BackdropEffect::Acrylic;
    if (name == "mica")      return ui::BackdropEffect::Mica;
    if (name == "mica_alt")  return ui::BackdropEffect::MicaAlt;
    return std::nullopt;
}

void CommandDispatcher::on_window_set_effect(const nlohmann::json& j) {
    auto* mw = registry_.get_window(j.value("id", ""));
    if (!mw) return;
    const std::string name = j.value("effect", "");
    if (auto effect = parse_effect(name))
        mw->win->set_effect(*effect);
}

void CommandDispatcher::on_window_clear_effect(const nlohmann::json& j) {
    if (auto* mw = registry_.get_window(j.value("id", "")))
        mw->win->clear_effect();
}

// ── WebView ───────────────────────────────────────────────────────────────────

void CommandDispatcher::on_webview_create(const nlohmann::json& j) {
    const std::string id     = j.value("id", "");
    const std::string win_id = j.value("window_id", "");
    const std::string mode   = j.value("mode", "window");

    webview::WebViewConfig cfg;
    cfg.devtools = j.value("devtools", false);

    webview::WebView* wv = nullptr;

    if (mode == "window") {
        wv = registry_.create_window_webview(id, win_id, std::move(cfg));
    } else if (mode == "view") {
        ui::ViewConfig vcfg;
        vcfg.position = { j.value("x", 0),      j.value("y", 0)       };
        vcfg.size     = { j.value("width", 400), j.value("height", 300) };
        wv = registry_.create_view_webview(id, win_id, std::move(vcfg), std::move(cfg));

        // Apply initial z-order; reorder_views sorts and restacks all overlays.
        if (wv)
            registry_.set_view_zorder(id, j.value("z", 0));
    }

    if (!wv) return;
    wire_webview_events(id, *wv);
}

void CommandDispatcher::wire_webview_events(const std::string& id, webview::WebView& wv) {
    wv.on_ready([this, id]() {
        emit({ {"type", "webview.ready"}, {"id", id} });
    });

    wv.on_load_start([this, id](webview::LoadEvent& ev) {
        emit({ {"type", "webview.load_start"}, {"id", id}, {"url", ev.url} });
    });

    wv.on_load_finish([this, id](webview::LoadEvent& ev) {
        emit({ {"type", "webview.load_finish"}, {"id", id}, {"url", ev.url} });
    });

    wv.on_load_failed([this, id](webview::LoadFailedEvent& ev) {
        emit({ {"type", "webview.load_failed"}, {"id", id},
               {"url", ev.url}, {"error", ev.error_description} });
    });

    wv.on_navigate([this, id](webview::NavigationEvent& ev) {
        emit({ {"type", "webview.navigate"}, {"id", id}, {"url", ev.url} });
    });

    wv.on_title_change([this, id](std::string title) {
        emit({ {"type", "webview.title"}, {"id", id}, {"title", title} });
    });

    wv.on_console_message([this, id](webview::ConsoleMessage& msg) {
        std::string level;
        switch (msg.level) {
            case webview::ConsoleLevel::Log:   level = "log";   break;
            case webview::ConsoleLevel::Info:  level = "info";  break;
            case webview::ConsoleLevel::Warn:  level = "warn";  break;
            case webview::ConsoleLevel::Error: level = "error"; break;
            default:                           level = "log";   break;
        }
        emit({ {"type", "webview.console"}, {"id", id},
               {"level", level}, {"text", msg.text} });
    });

    wv.ipc.on("*", [this, id](webview::Message& msg) {
        emit({ {"type",    "webview.ipc"},
               {"id",      id},
               {"channel", msg.channel},
               {"body",    msg.body} });
    });
}

